void Symbol_Filter(char S[])
{
	int Length;
	

	int i;
	i = 0;

	do
	{
		Length = strlen(S);
		if ('!' <= S[i] && S[i] <= '@') // ASCII characters with decimal value [32 ; 64]
		{
			for (int t = 0; t + i < Length; t++)
			{
				S[i + t] = S[i + 1 + t];
			}


		}
		else
		{
			i++;
		}


	} while (S[i] != '\0');


}

void Print_Sentence(char S[])
{
	int Length;

	Length = strlen(S);

	printf("\n\n");
	// Start Print process
	for (int i = 0; i < Length ; i++)
	{
		printf("%c", S[i]);

		if (S[i] == ' ')
		{
			printf("\n");
		}
	}
	printf("\n\n");
}


