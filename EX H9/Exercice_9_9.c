int Read_Word(char W[][32])
{
	int Length_word;
	char Word[32];
	printf("Please enter a word : ");
	scanf("%s%*c", Word);
	Length_word = strlen(Word);

	for (int i = 0; i < Length_word; i++)
	{
		W[i][0] = Word[i]; // ATTENTION 'W' is a matrix 'Word' is a array thus each iteration we copy each individual position from 'Word' to each row 'W'

		/*
			Example:

			if 'Word' = Elephant;

			then matrix 'W' will become

			e - - - - -
			l - - - - -
			e - - - - -
			p - - - - -
			h - - - - -
			a - - - - -
			n - - - - -
			t - - - - -
		
			leaving space to replace each character with it's morse code
		
		*/
		
	}
	
	return Length_word;


}

void Morse_Coder(char W[][32], const char* morse_registry[],int Length)
{
	
	for (int i = 0; i < Length; i++)
	{
		if ('A' <= W[i][0] && W[i][0] <= 'Z')
		{
			W[i][0] = W[i][0] + 32;
		}

		strcpy(W[i], morse_registry[W[i][0] - 97]);
	}


}

void Print_Morse(char W[][32],int Length)
{


	printf("\n Corresponding morse code: ");

	for (int i = 0; i < Length; i++)
	{
		printf("%s",W[i]);
		printf(" ");
	}



}





