int Read_Names(char Name[][Max_Length_Name])
{
	int Number_of_names;
	Number_of_names = 0;
	char N[Max_Length_Name]; // 'N' is a variable used as a intermediate

	printf("Please Input %d Names\n---------------------", Max_Names);
	printf("\nEnter Name Nr%2d (STOP = 'end') : => ", Number_of_names + 1);
	scanf("%s%*c", N);

	
	while (Number_of_names < Max_Names  && strcmp(N,"end") != 0)
	{
		
		int i = 0;
		int Length;
		do
		{
			Length = strlen(N);
			if ('!' <= N[i] && N[i] <= '@') // ASCII characters with decimal value [32 ; 64]
			{
				for (int t = 0; t + i < Length; t++)
				{
					N[i + t] = N[i + 1 + t];
				}


			}
			else
			{
				i++;
			}
		} while (N[i] != '\0');

		strcpy(Name[Number_of_names], N);

		Number_of_names++;

		if (Number_of_names < Max_Names)
		{
			

			printf("\nEnter Name Nr%2d (STOP = 'end') : => ", Number_of_names + 1);

			scanf("%s%*c", N);
		}
		

		

		
	}



	return Number_of_names;
}



