void FillMatrix(int M[][Colums])
{

	for (int i = 0; i < Rows; i++)
	{
		for (int t = 0 ; t < Colums; t++)
		{
		
			
		
			M[i][t] = (i + 1) * (t + 1);
		
		}
	}




}




void PrintMatrix(int M[][Colums])
{

	for (int i = 0; i < Rows; i++)
	{
		for (int t = 0; t < Colums; t++)
		{


			printf(" %3d |", M[i][t]);



		}
		printf("\n");
		for (int m = 0; m < Colums; m++)
		{
			printf("------");
		}
		printf("\n");
	}




}















