/* Function definitions */

void Fill_Matrix(int O[][2])
{

	// Fill in matrix 'Occurences'

	for (int i = 0; i < Max_different_Numbers; i++)
	{
		for (int t = 0; t < 2; t++)
		{
			O[i][t] = 0;

		}
	}


}

void Read_Numbers(int N[],int O[][2])
{
	printf("Please enter integers (end = 999): \n");

	int i = 0, Count = 0;
	
	scanf("%d%*c", &N[i]);
	

	while (N[i] != 999)
	{
		

		if (Count != Max_different_Numbers) // If there are more then the #number of different numbers we don't have to enter the statement
		{
			int insert = 1;
			for (int t = 0; t < Count ; t++)
			{
				if (N[i] == O[t][0]) 
				{
					insert = 0;
					
				}
			}

			if (insert == 1)
			{
				O[Count][0] = N[i];
				Count++;
			}

			
		}

		i++;
		scanf("%d%*c", &N[i]);
	}

}

void Count_Occurences(int N[], int O[][2])
{
	

	for (int i = 0; i < Max_different_Numbers; i++)
	{
		for (int t = 0; t < Max_Numbers; t++)
		{
			if (N[t] == O[i][0]) // If one of the numbers in array 'N' equals the numbers in matrix 'O' then we add 1 to row 'i' column 2
			{
				O[i][1] += 1;
			}
		}
	}


}

void Print_Occurences(int O[][2])
{
	printf("\n\nNumber | Occurence\n------------------\n");

	for (int i = 0; i < Max_different_Numbers; i++)
	{
		printf("  %3d  |  %3d  \n",O[i][0],O[i][1]);
	}
}



