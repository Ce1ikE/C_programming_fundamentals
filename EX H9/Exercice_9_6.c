void Read_Names(char Name[][Max_Length_Name])
{
	printf("Please Input %d Names\n---------------------", Max_Names);

	for (int t = 0; t < Max_Names; t++)
	{
		printf("\nEnter Name Nr%2d : => ", t + 1);
		scanf("%s%*c", Name[t]);
		int i = 0;
		int Length;
		do
		{
			Length = strlen(Name[t]);
			if ('!' <= Name[t][i] && Name[t][i] <= '@') // ASCII characters with decimal value [32 ; 64]
			{
				for (int m = 0; m + i < Length; m++)
				{
					Name[t][i + m] = Name[t][i + 1 + m];
				}


			}
			else
			{
				i++;
			}
		} while (Name[t][i] != '\0');

	}
}



