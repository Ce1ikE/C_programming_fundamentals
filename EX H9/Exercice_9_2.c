void CalculateRow(int M[][colums])
{

	
		for (int t = 0; t < colums; t++)
		{

			M[rows - 1][t] = M[rows - 2][t] + M[rows - 3][t];


		}

}


void PrintMatrix(int M[][colums])
{
	printf("\n\n And here is the same Matrix with a third row created by adding the first and second rows :\n");

	for (int i = 0; i < rows; i++)
	{
		for (int t = 0; t < colums; t++)
		{

			printf(" %2d |", M[i][t]);


		}
		printf("\n");
	}
}















