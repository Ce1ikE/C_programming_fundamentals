// Calculating the Mean of the entered integers
double Mean_Calculator(int x[], int S)
{

	double SUM;
	SUM = 0;

	for (int i = 0; i < S; i++)
	{
		SUM = SUM + x[i];
	}

	SUM = SUM / S;

	return SUM;

}

// Going through the array to see which is the biggest integer
int Max_Value(int x[],int S)
{
	int Max;
	Max = x[0];

	for (int i = 0; i < S; i++)
	{
		if (Max < x[i])
		{
			Max = x[i];
		}

	}

	return Max;
}



