void Dices_roll_simulation(int x[])
{
	int Dice_1, Dice_2, SUM;

	srand(time(NULL));

	for (int t = 0; t < Possible_results; t++)
	{
		x[t] = 0;
	}

	printf("This simulates the result of %d dices rolled %d times.\n\n", Number_of_dices,MAX_Rolls);

	for (int i = 0; i < MAX_Rolls; i++)
	{
		SUM = 0;
		for (int i = 0; i < Number_of_dices; i++)
		{
			Dice_1 = rand() % 6 + 1;
			SUM += Dice_1;
		}
		
		
		x[SUM - Number_of_dices]++;

	}

}






void Print_results(int x[])
{
	int Max;
	Max = x[0];


	for (int l = 0; l < Possible_results; l++)
	{
		if (Max < x[l])
		{
			Max = x[l];
		}
		 
	}

	printf("\n                        ");
	for (int m = 0; m < Max; m++)
	{
		
		if (m % 5 == 0)
		{
			printf("%d",m);
		}
		else
		{
			printf(" ");
		}
		
	}
	printf("\n------------------------|");
	for (int i = 0; i < Max; i++)
	{
		printf("-");
	}
	printf("\n                        |\n");

	for (int t = 0; t < Possible_results; t++)
	{
		printf(" Occurence of Number %2d | ", t + Number_of_dices);
		for (int i = 0; i < x[t]; i++)
		{
			printf("\033[32m\xDB\033[0m");
		}
		printf("\n                        |\n");
	}
	

}





