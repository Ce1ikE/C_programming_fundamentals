void Random_generator(int x[])
{

	srand(time(NULL));

	int i;
	i = 0;
	int Duplicate;


	while (i < Max)
	{
		
		do
		{
			Duplicate = 0;
			x[i] = rand() % 42 + 1;
			for (int t = 0; t < i; t++)
			{
				if (x[i] == x[t])
				{
					Duplicate = 1;
				}
			}

		} while (Duplicate);
		i++;
	}

}








