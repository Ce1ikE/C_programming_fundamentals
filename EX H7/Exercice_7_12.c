/* Function Definitions */

void Read_Array(int A[])
{
	printf("\nEnter %d integers: ", MAX);
	for (int i = 0; i < MAX; i++)
	{
		scanf("%d%*c", &A[i]);
	}
}


int Control_Order(int A[])
{
	int Check;

	for (int i = 1; i < MAX; i++)
	{
		if (A[i - 1] > A[i])
		{
			Check = 0;
		}
		else
		{
			Check = 1;
		}
	}

	return Check;
}

void PrintOrderedArray(int A1[], int A2[])
{
	
	int s = 0,i = 0;
	while (i != MAX && s != MAX)
	{
		if (A1[i] <= A2[s])
		{
			printf("\033[32m");
			printf("%d ",A1[i]);
			printf("\033[m");
			i++;
		}
		if (A1[i] >= A2[s])
		{
			printf("\033[34m");
			printf("%d ",A2[s]);
			printf("\033[m");
			s++;
		}
	}

	if (i != MAX)
	{
		printf("\033[32m");
		printf("%d ", A1[i]);
		printf("\033[m");
		i++;
	}

	if (s != MAX)
	{
		printf("\033[34m");
		printf("%d ", A2[s]);
		printf("\033[m");
		s++;
	}


	
}


