void rand_Number(int * a,int * b)
{
	

	

	for (int i = 0; i < 2; i++)
	{
		int Sign = rand() % 2;

		if (i == 0) // First for 'a'
		{
			if (Sign == 0) // Positive number
			{

				*a = rand() % Max_Rand + 1;

			}
			else // Negative number
			{
				*a = -rand() % Max_Rand + 1;
			}
		}
		else
		{
			if (Sign == 0) // Positive number
			{

				*b = rand() % Max_Rand + 1;

			}
			else // Negative number
			{
				*b = -rand() % Max_Rand + 1;
			}
		}
		
	}
	


}






