int Read_Array(int A[])
{
	int Count = 0, Error = 0;

	printf("Enter %d numbers in ascending order:\n", MaxNumbers - 1);
	scanf("%d%*c", &A[Count]); // doing one scan outside the "While" loop in order to compare  one position to the previous one
	Count++;

	do
	{
		

		scanf("%d%*c",&A[Count]);

		Count++;

	} while (Count < MaxNumbers - 1 && A[Count - 1] > A[Count - 2]);

	if (Count != MaxNumbers - 1)
	{
		printf("\n\n\n\t ERROR numbers not entered in ascending order \n\t\t\t PROGRAM STOPPED");
		Error = 1;
	}

	return Error;
}




