void Insert_NumberInArray(int A[], int N)
{
	int i = 0;

	while (A[i] < N && i < MaxNumbers - 1)
	{
		i++;
	}

	for (int t = 0; t < MaxNumbers - i; t++)
	{
		A[MaxNumbers - t] = A[MaxNumbers - t - 1]; // Starting from the back of the array we take every element and put it a position further until we come at the position where we need to stop

	}

	A[i] = N;
	
	

}



void Print_NewArray(int A[], int N)
{
	printf("\n Here is the full array in ascending order with your number in it :\n\n");
	for (int i = 0; i < MaxNumbers; i++)
	{

		if (A[i] == N)
		{
			printf(" => %d <= ", A[i]);
		}
		else
		{
			printf(" %3d ", A[i]);
		}

	}

}






