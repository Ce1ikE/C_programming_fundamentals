void ReadFile(void)
{

	FILE * fs; // fs == filesource
	char FileSource[32];

	printf("\nWhich file do you want to open? => ");
	scanf("%s%*c", FileSource);
	fs = fopen(FileSource ,"r");

	// Checking if path to file exists
	while ( fs == NULL)
	{
		int Answer;
		printf("\n\n\t\033[31mERROR\033[m\nUnable to open file \"%s\"!\n\nContinue(Yes = 1 ; No = 0) ", FileSource);
		scanf("%d%*c", &Answer);

		if (Answer == 1)
		{
			printf("\nWhich file do you want to open? => ");
			scanf("%s%*c", FileSource);
			fs = fopen(FileSource, "r");
		}

		if (Answer == 0)
		{
			exit(-1);
		}
	}

	// Printing each line to the terminal
	
	char Line[LineLength];
	int Counter = 0;
	
	printf("\n\nOUTPUT from file \"%s\"\n\n",FileSource);
	while (fgets(Line,LineLength,fs) != NULL)
	{
		printf("(%d) ",Counter++);

		printf("%s", Line);
	}

	fclose(fs);




}




