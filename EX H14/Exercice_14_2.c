void CheckFile(FILE * fs, char Filesource[])
{
	while (fs == NULL)
	{
		int Answer;
		printf("\n\n\t\033[31mERROR\033[m\nCouldn't find file : \"%s\"  \nTry again?(Yes = 1 , No = 0)\n=> ",Filesource);
		scanf("%d%*c", &Answer);

		if (Answer == 1) // Yes
		{
			printf("\nWhich file do you want to copy? => ");
			scanf("%s%*c", Filesource);
			fs = fopen(Filesource, "r");
		}

		if (Answer == 0) // No
		{
			exit(-1);
		}
	}
}


