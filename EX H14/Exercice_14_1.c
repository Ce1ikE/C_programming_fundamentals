void Read_File(void)
{
	FILE * f;

	char NameFile[32];

	printf("\nWhat is the name of the file? => ");
	scanf("%s%*c", NameFile);
	f = fopen(NameFile, "r");

	if (f == NULL)
	{

		printf("\n\n\t\033[31mERROR\033[m\nUnable to open file\n");

		exit(-1);

	}
	else
	{
		char TextLine[LengthString];
		printf("\n\n\t\t");
		while ((fgets(TextLine, LengthString, f)) != NULL)
		{
			printf("%s", TextLine);
		}
	}

	fclose(f);
}


