int main(void)
{
	/* Exercice 5.3.16.

	Assignment:
	Write a program that draws a tower upside down using the 
	ASCII character 219 (\xDB). The tower consists of a sequence of squares. 
	The top square has a side defined by the user. The next 
	square is 2 blocks smaller, the next one is again 2 blocks smaller, …
	New towers are drawn until the user enters ‘n’.


	*/

	Hello();

	printf("\n\t----\tDit is oefening 5.3.16. van 'Cprogrammingfundamentals2020' Pg84\t----\n\n\n\n");

	int width;
	char Answer = 'y';

	printf("This program builds a tower upside down!\n How wide should the tower be ?:  ");
	scanf("%d%*c", &width);

	while (width < 3)
	{
		printf("\n\n\tLACK OF MATERIALS\n\n");
		printf("\n How wide should the next tower be ?:  ");
		scanf("%d%*c", &width);
	}



	while (Answer == 'y')
	{
		//TOWER CONSTRUCTION SEQUENCE
		for (int t = 0; t < width; t++)// Tells the number of squares
		{
		
			for (int i = 0; i < width - 2; i++)// Puts the pointer onto the next line and we did "-2" to give the "square" more a feeling of a real looking square 
			{
				for (int s = 0; s < t; s++)
				{
				printf(" ");// Prints a space
				}
				

				for (int n = 0; n < width - t; n++)// Prints a line
				{
				printf("\xDB");// Prints a block
				}
				
			printf("\n");
			
				
			}

		
		width--;

		}


		printf("\n Do you want to build another tower ? (y / n)\t");
		scanf("%c%*c",&Answer);

		if (Answer == 'y')
		{
			printf("\n How wide should the tower be ?:  ");
			scanf("%d%*c", &width);	
			while (width < 3)
			{
				printf("\n\n\tLACK OF MATERIALS\n\n");
				printf("\n How wide should the next tower be ?:  ");
				scanf("%d%*c", &width);
			}
		}
		
	}


	printf("\n\n\t\t END OF CONSTRUCTION\n\n\n");

	Bye_Message();

	return 0;
}





