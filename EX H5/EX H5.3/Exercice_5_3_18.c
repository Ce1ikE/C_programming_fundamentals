int main(void)
{
	/* Exercice 5.3.18

	Assignment:
	 Write a program that determines the maximum out of 10
	numbers entered by the user. The program also prints the number of
	times the maximal value was entered.

	*/

	Hello();

	printf("\n\t----\tDit is oefening 5.3.18 van 'Cprogrammingfundamentals2020' Pg85\t----\n\n\n\n");

	int Number, Count, Max;

	printf("\nEnter Nr.%2d: ",1);
	scanf("%d%*c", &Number);
	Max = Number;
	Count = 1;
	for (int i = 1; i < 10; i++)
	{
		printf("\nEnter Nr.%2d: ", i + 1);
		scanf("%d%*c", &Number);

		if (Number > Max)
		{
			Max = Number;
			Count = 1;
		}
		else
		{
			if (Number == Max)
			{
				Count++;
			}
		}


	}

	printf("\n\nThe maximum is: %d\nAnd was entered: %d times", Max, Count);

	Bye_Message();

	return 0;
}





