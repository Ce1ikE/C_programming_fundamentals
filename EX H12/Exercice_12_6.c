void Print_SJW(short int N)
{

	short int Nibble;

	Nibble = N >> 6; // We forced the user to use a number that bitwise has always leading 0's that is the reason we don't mask we can still mask with 0x000C if we want to
	
	/*
	
		Example:
		Number:			0000 0000 1111 1111 (This is the maximum number the user can choose since we have to fit enter a number that fits the register (1 Byte))
		shift right:	0000 0000 0000 0011 (As you can see the MSB bits are 0's so shifting to the right will not result with 1's in front of our bits of interest)

	*/

	printf("\n\nThe SJW equals => DEC format: %hd   HEX format: %x", Nibble, Nibble);


}

void Print_BRP(short int N)
{

	short int Nibble;

	Nibble = N & 0xFF3F; // We mask with 1111 1111 0011 1111

	/*
		
		Example:
		Number:			0000 0000 1111 1111
		Mask:		&	1111 1111 0011 1111	
					-----------------------
		BRP:			0000 0000 0011 1111

	*/

	printf("\n\nThe BRP equals => DEC format: %hd   HEX format: %x", Nibble, Nibble);

}

void Output(short int Number)
{
	int Count = 0;

	printf("\n\n(binary representation) OUTPUT : ");
	for (int i = 1 << 15; i > 0; i /= 2) // we are starting with the MSB in order to work our way down to the LSB. The reason behind it is because we are printing from left to right.
	{
		if (Number & i)   // 1 = true and 0 = false so simply putting 'Number & i ' would also statisfy
		{
			printf("1");
		}
		else
		{
			printf("0");
		}

		Count++;

		if (Count == 4)
		{
			printf(" ");
			Count = 0;
		}
	}

}



