short int Create_seed(void)
{
	short int s;

	srand(time(NULL));

	s = rand() % 5000 + 1; // [1;4999]
	
	return s;

}



short int Create_Rand(short int R)
{
	static short int Help;

	Help = (R >> 14) ^ (R >> 13);

	R = R << 1;

	R = R | Help;

	R = R & 32767; // 0111 1111 1111 1111

	return R;
}


