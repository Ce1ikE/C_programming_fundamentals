void Print_LeftShiftBit(short int N)
{

	printf("\n\nINPUT : %hd", N);

	short int nibble;

	nibble = (N >> 12) & 0x000F; // Taking the 4 MSB and shifting them right by 12 bits and then we mask the other 12 bits by a 'and' bit operation in case the MSB was a 1
	N = (((N << 4) & 0xFFF0) | nibble); // First we shift all bits in N to the left by 4. Then we mask the 4 LSB. And at last we add the 4 bits previously obtained with a 'or' bit operation

	printf("\nOUTPUT : %hd", N);


}





