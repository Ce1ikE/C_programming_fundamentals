void Calculater(int * S, int * M, int * H)
{
	// 1st Calculation to highest variable
	*M = (*S - (*S) % 60) / 60;

	*H = (*M - (*M) % 60) / 60;

	// Backwards calculation to lowest variable
	*M = *M % 60;


}

void Print(int S, int M, int H)
{

	printf("The entered time of %d seconds equals %d hours, %d minutes and %d seconds.",S,H,M,(S % 60));


}


