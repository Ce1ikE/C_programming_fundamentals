void Read(char W[])
{
	printf("\n\nEnter a word or a series of letters: ");
	scanf("%s%*c", W);

	int Length = strlen(W);

	while (Length < 2) // Words smaller than 2 characters can't be entered
	{
		printf("\n\n\tERROR\n\nToo short please reenter a word or a series of letters: ");
		scanf("%s%*c", W);
		Length = strlen(W);
	}


}

void Upper_2_Lower_Converter(char W[]) // Converting Capital letters to small letters just in case since capital and small letters don't have the same ASCII value
{

	int Sentence_length;
	Sentence_length = strlen(W);

	for (int i = 0; i < Sentence_length; i++)
	{
		if ('A' <= W[i] && W[i] <= 'Z')
		{
			W[i] = W[i] + 32;
		}

	}

}

void Print(char W[],char * Largest,char * Smallest)
{

	int Length = strlen(W);
	*Smallest = W[0];
	*Largest = W[0];

	for (int i = 1; i < Length; i++) // Seeking the largest and smallest letter is just like seeking for a min and MAX
	{
		if (W[i] < *Smallest)
		{
			*Smallest = W[i];
		}
		
		if (W[i] > *Largest)
		{
			*Largest = W[i];
		}
	}
	printf("\n%c%c", W[0], W[1]);
	for (int i = 0; i < Length-1; i++)
	{
		if (W[i] < W[i + 1]) // If the second letter is larger then the first we print the second one
		{
			printf("%c", W[i + 1]);

		} 
		else
		{
			printf("%c", W[i]);
		}
	}

}

