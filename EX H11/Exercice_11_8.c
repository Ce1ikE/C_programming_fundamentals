void CalcPrint(int * N, int * L, int * S_O, int * S_E)
{
	printf("Multiplication table of %2d \n ---------------------------------------- \n", *N);

	for (int i = 0; i < (*L)/(*N); i++)
	{
		printf("\n %2d x %2d = %3d", i + 1, *N, (*N) * (i + 1));

		if (((*N) * i) % 2 == 0)
		{
			(*S_E) += (*N) * i;
		}
		else
		{
			(*S_O) += (*N) * i;
		}
	}


}



